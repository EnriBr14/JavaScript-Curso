//Sentencia if y else

let dia = 3
if(dia === 1){
    console.log(
        console.log("Lunes")
    )
}
else if(dia === 2){
    console.log("Martes")
}
else if(dia === 3){
    console.log("Miercoles")
}
else if(dia === 4){
    console.log("Jueves")
}
else if(dia === 5){
    console.log("Viernes")
}

//Usando la sentencia switch
switch (dia){
    case 1:
        console.log("Lunes Switch")
        break
    case 2:
        console.log("Martes Switch")
        break;
    case 3:
        console.log("Miercoles Switch")
        break;
    case 4:
        console.log("Jueves Switch")
        break;
        case 5:
            console.log("Viernes Switch")
        break;
}